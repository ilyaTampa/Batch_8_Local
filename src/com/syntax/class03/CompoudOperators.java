package com.syntax.class03;

public class CompoudOperators {

	public static void main(String[] args) {
		
		int num=100;
		num=num+10;
		System.out.println(num);
		
		num=num+200;
		System.out.println(num);
		
		int myNum=100;
		myNum+=10;//110
		System.out.println(myNum);
		
		myNum+=200;//310
		System.out.println(myNum);
		
		myNum-=100;//210
		System.out.println(myNum);
		
		myNum/=10;//21
		System.out.println(myNum);
		
		myNum*=1000;//21000
		System.out.println(myNum);
		
		myNum%=100;//0
		System.out.println(myNum);
		
		
	
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
