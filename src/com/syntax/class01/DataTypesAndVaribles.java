package com.syntax.class01;

public class DataTypesAndVaribles {

	public static void main(String[] args) {
		
		//primitive datatypes
		//numeric
		
		// type name = assign value
		byte variable1=127;//-128 to 127
		
		short varible2=100;//-32768 to 32767
		
		int anyName=1000;
		
		long varible3=100000;
		
		//to store floating numbers
		
		float var1=12.99f;
		double var2=9.99;
		
		// represent yes or no (true or false)
		boolean myVariable=true;
		boolean anotherVariable=false;
		
		//to represent single character
		char myBox='C';
		char myBox2='&';
		char myBox3='7';
		
		
		System.out.println(myBox3);
		

	}

}
