package com.syntax.class08;

public class Task1 {

	public static void main(String[] args) {
		// Print numbers from 1 to 100 in 1 line with space
		
		for (int a=1; a<=100; a++ ) {
			System.out.print(a+" ");
		}
		System.out.println();
		
		for (int a=100; a>0; a-- ) {
			System.out.print(a+" ");
		}
	}

}
