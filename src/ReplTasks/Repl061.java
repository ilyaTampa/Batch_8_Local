
public class Repl061 {

	public static void main(String[] args) {
		// Write a program that uses for loop to prints multiplication of
		// number 3 from 1 to 10

		int num, num1, mult;
		num = 3;
		for (num1 = 1; num1 <= 10; num1++) {
			mult = num * num1;
			System.out.println(num + "*" + num1 + "=" + mult);
		}

	}

}
